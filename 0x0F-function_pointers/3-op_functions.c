#include "calc.h"
/**
 * op_add - adds two numbers
 * @a: first number
 * @b: second number
 * 
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substracts two numbers
 * @a: first number
 * @b: second number
 *
 * Return: substraction of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * Return: multiplies a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divison of a by b
 * @a: first number
 * @b: second number
 *
 * Return: divison of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modules a by b
 * @a: first number
 * @b: second number
 *
 * Return: remainder of the division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
